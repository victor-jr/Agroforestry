<h2>Jerbal ippān armej ro</h2>

<ul>
<li>L̗ōmn̗ake ebaj et ke elōn̄ wōjke rōkar juaki tōre ko rōmoot l̗o̗k, em ejaake juon pepe eo me ekūrm̗ool l̗o̗k ekkar n̄an pel̗aak eo. Armej ro rōkōn̗aan jet jikin ko rōmaron̄ lo lo̗jet eo kab rōlladikdik? Ewi wāween an mar ko kab wōjke ko jelōt privacy? Ia eo ilo parijet eo em̗ōj an jino jako l̗o̗k? Ia ko me el̗ap aer aikuj in kōjparoki? Armej ro ilo jukjuk-im-pād eo rōmel̗el̗e ke kōn kōjparok parijet eo?</li>
<li>Ilo m̗anit, kepaak im kōnono ippān al̗ap ro kab ri-tōl ro</li>
<li>Bar kōm̗m̗an bwe ajiri ro, ri-jerbal ro rej lali pel̗aak ko, em jabdewōt eo me emaron̄ jelōt er bwe ren bōk kun̗aer</li>
<li>Jet armej rej niknik “karreoiki” wāto ko aer ilo aer rakūji (ak totaki) ujooj ko, mar ko em wōjke jiddik ko, ak men in ekōm̗m̗an bwe bwidej eo en jedmatmat; wōjke ko make rōjabwe. Kapilōke armej ro bwe ren lo aiboojoj in ak unin an jan̄n̄ōr kab marilik kein kar eddek ijeko rej eddek ie em kōm̗m̗an bwe en lukkuun in bukwekwe.</li>
<li>Jutak likin em kōmmoururi kakien ko me rej kōmlōt bwe armej ren kōjparok jan̄n̄ōr em marilik ko rej eddek tōrerein parijet eo (kōmo̗ rakūji, juaki, em kōm̗akūti)</li>
<li>Jutak likin em kōmmoururi kakien ko me rej kōmlōt bwe en mo̗ rupi pedped ko em kūbwiji arin kab likin aelōn̄ ko n̄an kōm̗m̗an bok im dekā n̄an wia kaki</li>
</ul>

<h2>Jerbal ippān pel̗aak eo ekkar n̄an karōk in</h2>

<ul>
<li>Kōjparoki ijeko rōbukwekwe em rōmarmari em rōkar baj pād wōt jān m̗okta. Jet wōjke em mar rōlukkuun in rūtto em ebōk elōn̄ iiō n̄an juaki em bar katōki. N̄e wōjke kein rōnaaj jako, em bwidej eo em̗ōj an lo̗jet eo kutake, emaron̄ in jab em̗m̗an n̄an bar ekkat ejja ijen̗</li>
<li>Jipan̄ pel̗aak eo bar ekkat bwe en maron̄ bar wal̗o̗k juon jikin eo me ebukwekwe em emarmare. Ilo jet jikin, okar ko em ine in wōjke ko rej make bar eddek. Kōjparoki ijeko me wōjke ko kab mar ko rej make eddek ie ak wōjke ko me kwōj kōn̗aan bwe ren pād wōt ilo tiljek āinwōt am̗ kōjparoki ine in wōjke ko rōkāāl. Kōkkal̗l̗eiki em m̗wijiti.</li>
<li>Kōtl̗o̗k bwe marilik ko ekoba wōjke ko ren eddek, bwe en bukwekwe em okran wōjke im mar kein ren dāpij bwidej eo, jālitaki wāto ko jān kōto em ren rupi n̗o ko rej tōpar āne</li>
<li>Wōjke ko rōl̗l̗ap rōmaron̄ in kōm̗m̗an bwe en l̗ap l̗o̗k an jedmatmat parijet eo n̄an jorrāān ko wal̗o̗k tok jān lo̗jet eo n̄e rōnaaj ekkat epaake l̗o̗k meto, kōnke rōnaaj kapidodouki bwidej eo n̄e rookjak, em ae eo ej depet āne emaron̄ kutake bwidej eo jān bul̗ōn okar in wōjke ko em kāā-er</li>
<li>Kōjparoki menin mour em menin eddek ko kajjojo ijeko rōkar baj pād wōt ie ekkar n̄an karōk ko aer ijeko <a href="strand-vegetation">mar in ioon kappe</a> ko rej pād ie em ijeko elōn̄ <a href="mangroves">jon̄</a> ie.</li> 
<li>Keememej bwe wōjke ko kam̗ōjn̗oik l̗o̗k an lo̗jet eo kutake bwidej eo em kōjakoik l̗o̗k ak eban lukkuun in kabōjrak an lo̗jet eo kutake em kōjakoik l̗o̗k bwidej eo. For a more complete explanation of coastal change, ask CMI for the "Coastal Change in the Pacific Islands" book or download it <a href="http://www.reefresilience.org/wp-content/uploads/Gombos-et-al.-2014-Coastal-Change-in-the-Pacific-Islands.pdf">here</a> (NOTE this is an 8MB file)</a>.</li>
</ul>

