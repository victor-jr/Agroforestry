<h3>Tu-ia n̄an ekkat: kajjojo kein ekkan rāinjuon jān doon rej eddek ilo kajjojo pel̗aak ko</h3>
<ul>
<li><a href="http://www.hawaii.edu/cpis/MI/plants/bulabol.html">Eoeak ak būl̗abōl</a> ko rej n̗a etaer fringe species kōnke rōmaron̄ in eddek metwan l̗o̗k ijeko jon̄ ko rej eddek ie. Ekkat ‘propagule’ ak ine ko ineen jon̄ kein ijeko iol̗ap in kappe ikōtaan ibwij im pāāt, ijeko ekkā an naaj ibwiji aolep kajjojo raan.  El̗an̄n̄e enaaj iiō in El Nin̄o, utiej in lo̗jet enaaj kanooj ettā, kōn menin ekkat ijeko rōpijpij, kōnke el̗an̄n̄e enaaj bar jem̗l̗o̗k El Nin̄o, utiej in lo̗jet n̄an jon̄an eo ekkā an wal̗o̗k, enaaj m̗wilal̗ l̗ok.</li>
<li><a href="http://www.hawaii.edu/cpis/MI/plants/bulabol_(konpat).html">Kōnpat ak būl̗abōl</a> im jon̄ ekkā aer eddek ilo pat ko ilo M̗ajel̗.  Kwōmaron̄ kajjion̄ ekkat elōn̄ ine in jon̄ ijeko repaake parijet jān eoeak ko, ijeko rōban l̗ap aer ibwij ak pāāt.</li>
<li><a href="http://www.hawaii.edu/cpis/MI/plants/kimeme.html">Kimeme</a> ko rej n̗a etaer interior species.  Ekkā aer eddek ilo pat ko.  Kwomaron̄ ekkat ine in kimeme ko ijeko repaake parijet, ijeko wōt emaron̄ naaj ibwiji ilo iien iōl̗ap-lep.</li>
<li><a href="http://www.hawaii.edu/cpis/MI/plants/kone.html">Kōn̄e</a> ej bar m̗ōttan jon̄ eo emaron̄ bar eddek ijeko repaake parijet ak rōmaron̄ eddek ijeko el̗ap aer ibwij.</li>
</ul>

