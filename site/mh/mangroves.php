<p><a href="http://www.hawaii.edu/cpis/MI/VegTypes/Mangrove.html">Jon̄</a> ko rej wōjke ko em kain mar ko me rōmaron̄ in eddek ilo jo̗o̗l̗. Ilo M̗ajel̗, ekkā loi ijeko me rōtutu em rej pād ibul̗ōn aelōn̄ ko (ak pat ko). Rōmaron̄ in bar eddek ijeko rōbukwekwe kōn mar em wōjke ium̗win dān ilo iien ibwij itōrerein kappe ko. Mijel in ra in jon̄ ko kab okraer rōjipan̄ ilo aer rupi n̗o ko m̗okta jān aer tōpar kappe.</p>

