<h2>Tu-ia n̄an ekkat jon̄</h2>

<p>Elukkuun in pen n̄an bar kōkat jon̄. Elukkuun in aorōk n̄an kōjparoki jon̄ ko me em̗ōj aer de eddek. N̄e kwōj kōn̗aan kajjion̄ ekkat, jino ijo me ewōr “propagules” (mel̗el̗e in ine ko me em̗ōj an lōn̄ okraer kadede m̗okta jān aer kar wōt lo̗k). Bar kajjion̄ ekkat ijeko me jon̄ ko rōkar eddek ie m̗oktal̗o̗k, em ijeko dān in lo̗jet eo ejablur im pāāt.</p>

