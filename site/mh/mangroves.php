<p><em>Ekkatak ko jān <a href="acknowledgements.php">Sleeper Sared</a></em></p>

<p>Ilo jilu iaan āne ko, Sleeper ear ekkat jon̄ itōrerein parijet ko tu-ar aitokan juon m̗ail.  L̗ein eaar katōk jon̄ kein ilo emān l̗ain, kōn 2-3 ne kōtaer jān doon.  Eaar bareinwōt ain ine ko raar eo̗n̄ im kajju katōki ilo bok ak lūm̗.  Ālikin juon iiō, 75% (jilu kuwōta) in ine kein raar eddek.  Sleeper eaar ba ke wūjooj in lo̗jet ej menin kallikkar ke lo̗jet eo ebwe an lur/l̗ae im bwe jon̄an lim̗ eo ie.</p>
<p>Ilo bar juon iaan āne ko, aolep jon̄ eo ie rōmij, kōnke ae ko ie el̗ap aer kajoor.</p>
<p>Sleeper eaar ba:  El̗an̄n̄e ewōr ae im jabwe jon̄an lim̗ eo n̄an dāpij ine ko ilo tōre in ekkat, eddoor dekā ak ok ko em̗ōj kanni kōn dekā.  Kōttar an ae ko pooktok lim̗ ko.  Ālikin, katōk ine ko ilo lim̗ kein.</p>
<p>Sleeper eaar ba:  Bar juon wāween n̄an ijeko ejjab l̗ap lim̗ ie ej n̄an kōjerbal pākij peba ko āinwōt kilin pāāk in cement ak rice.  (Pākij peba ko ekkā jān im̗ōn wia ko rōm̗ōjn̗o̗.)  Kanne pāāk kein kōn bok im wūjooj in lo̗jet im katōk ine ko ilowaaer im door m̗okta ilo juon jikin em̗ōj kōjenolo̗ke.  Rōnaaj pojak el̗l̗an̄n̄e enaaj wal̗o̗k okar ko ilowaan wōt pāāk kein, bōtaab ej aikuj m̗okta jen an okar kein kōjar pāāk kein ak eddek ilo bwidej.  Kōm̗akūt ine kein n̄an ron̄ ko ijeko relim̗lim̗ ak ikōtaan dōkā ko.</p>

<p><em>Karōk n̄an ekkat eoeak ilo parijet ko ilo Ailuk</em></p>

<p>Ailuk Atoll Marine Resource Management Advisory Committee ear itok-limo in ekkat jon̄ ilo parijet ko ilo Ailuk n̄an kadikl̗o̗k an rōm tōrereen parijet ko ie.  Rhizophora kar maron̄ kelete bwe en kain jon̄ eo ekkar n̄an katōki ilo parijet kein, bōtaab enaaj kar aikuj in itok jen aelōn̄ ko jet.  Em̗ōj karōk ijo jikin ekkat (1) ijeko ejjab l̗ap an ibwij im pāāt (2) ijeko relur jen kōto im n̗o (3) ijeko epidodo lim̗ ko ie em̗m̗an n̄an ekkat ine ko (4) jen ijeko rej ibwij iaarl̗o̗k im ejjab likl̗o̗k.  “Natural Resource Assessment Survey” ej kwal̗o̗k l̗ōmn̗ak im karōk ko n̄an jerbal in ekkat jon̄ in.</p>

<p><em>Ekkat jon̄ ilo Kiribat</em></p>

<p>Kiribat, einwōt M̗ajel̗, ej juon lal̗ kōm̗m̗an jen elōn̄ atoll, ijeko walōn̄tak in l̗ābōl̗ in lojet im an rōml̗o̗k parijet ko ej juon menin abn̗ōn̗ō el̗ap.  Jon̄ ko ie rej menin jeraam̗m̗an el̗ap ilo Kiribat im būrookraam̗ in ekkat jon̄ ej wōnm̗aanl̗o̗k wōt enan̄in ron̄oul iiō.  Rhizophora stylosa (juon kain jon̄ epaakel̗o̗k eoeak) ej juon eo el̗ap an aorōk n̄an ekkat.  Ine ko rej katōki itōreen parijet ko im ial ko ikōtaan ane.  N̄an  jidik mel̗el̗e ko ikijjeen jerbal in ekkat jon̄ ko ilo Kiribat, lale newsletter iō.</p>

