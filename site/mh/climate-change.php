<h1>Long-Term Effects of Climate Change in the Marshall Islands</h1>

<p>What changes will affect agroforestry in the Marshalls and when? We still do not know what choices the world will make and exactly how the air and oceans will change in the long term. Let’s think about what the climate will be during the next forty years, because that is the time period that a coconut tree is generally considered to be productive. Forty years allows time for coconuts, breadfruit and other trees to provide crops for children and grandchildren!</p>

<ul>
    <li><p>The natural cycle of <a href="enso.php">La Niña and El Niño years (ENSO)</a> will continue. The effects of these events (storms, rain and drought, sea level) are temporary (lasting 1–2 years) but strong. Their effects will be more noticeable than climate change for the next forty years. Scientists are not sure yet whether climate change will affect how La Niña and El Niño events happen.</p></li>

    <li><p>Worldwide sea levels are now rising at least 0.3 inches per year, so they will likely rise at least 12.6 inches within forty years. In addition, sea levels may temporarily rise by 12 inches during a La Niña and drop by 5 inches during an El Niño event in the Marshalls. Sea level rise is also added to the effect of each king tide and storm surge, making inundation events more frequent. Generally high sea levels will increase the salinity of groundwater. This makes it important to choose crops that are relatively <a href="/">salt-tolerant</a>. Plant agroforests where <a href="/">inundations are less frequent</a>. For more information on the effects of sea level rise, see <a href="http://pcep.prel.org/wp-content/uploads/2014/08/PCEP-Sea-level-final.pdf">Sea Level Rise in the U.S. Affiliated Pacific Islands</a>.</p></li>

    <li><p>Air temperatures will continue to get a little warmer in the Marshalls. Hot days make trees and crops thirsty, so choose trees and crops that can <a href="/">tolerate dry conditions</a>.</p></li>

    <li><p>Rainfall has been generally decreasing in the Marshalls, but scientists believe it will increase in the future. However, within just the next forty years, any changes in rainfall will mostly be a result of the natural variability of rainfall in the Marshalls, especially the El Niño/La Niña cycle (ENSO). Therefore, choose trees and crops that can tolerate a variety of conditions and <a href="/">plant a variety of crops</a>.</p></li>
</ul>

