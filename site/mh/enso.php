<h1>The El Niño Southern Oscillation (ENSO)</h1>

<p>El Niño Southern Oscillation (ENSO) eo ej juon tōre eo me mejatoto eo ejelōt M̗ajel̗ (em elōn̄ jikin ko jet pel̗aak in lal̗ in).</p>

<ol start="1">
    <li>N̄e ej ko̗o̗to em em̗m̗an lan̄, mel̗el̗e in bwe eban lōn̄ El Niño ak La Niña</li>
    <li>N̄e ej m̗ōjn̗o̗ l̗o̗k kūtwōn kōto ej ko̗o̗to jān jon̄an an m̗ōjn̗o im dik, ak ejjel̗o̗k, mel̗el̗e in bwe ej juon iiō eo enaaj lōn̄ El Niño</li>
    <li>N̄e ej kajoor l̗o̗k kūtwōn kōto eo ej ko̗o̗to jān jon̄an an kajoor im l̗ap, mel̗el̗e in bwe ej juon iiō eo enaaj lōn̄ La Niña</li>
</ol>

<p>Bwil in im m̗ōl̗o in kōto im lo̗jet eo rōjelōt wōt, l̗an̄ ko, iien an det jilōn̄lōn̄, im an wanlōn̄ tak lo̗jet eo.</p>

<p><h2>El Niño</h2> Iiō in El Niño (Year 0) ekkā an wōt em wal̗o̗k l̗an̄. L̗o̗k iiō en̗ ālikin an El Niño (Year 1) ekōn l̗ap an det jilōn̄lōn̄ im m̗ōrā. L̗an̄ ko im iien det jilōn̄lōn̄ ko rej jab em̗m̗an, ak kōnke jejel̗ā n̄āāt rej bōk jikier im jemaron̄ kallikkar katu eo aer, ro rej kōkat rōmaron̄ kōm̗m̗ani wāween ko rōkkar n̄an kōjparoki keinikkan im m̗ōn̄ā ko rōkar katōki bwe ren jab jorrāān.</p>

