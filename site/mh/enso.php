<p><h2>La Niña</h2> “Watch” im “advisory” ko rej bar kōjel̗ā kaki ilo iiō ko me enaaj wōr La Niña ie. Ijoke, ālikin iien eo naan in keean̄ eo kijjien La Niña em̗ōj kōjel̗ā kake (eo im ej iturin l̗o̗k allōn̄ in Juun ilo Year 0), ekwe eban kanooj in l̗ap det ak ikkutkut l̗an̄ n̄an katuiki. N̄an Neutral im La Niña, ro rej kōkat rej aikuj in kōjparoki keinikkan ko kōtkaer ilo aer kōjerbal kapeel im imminene ko aer āinwōt jet ri-M̗ajel̗ em ilo aer kōjerbal wāween ko jān ri-likin ro.</p>

