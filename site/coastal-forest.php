<h2>Work with people</h2>

<ul>
<li>Consider why plants have been cleared in the past, and make a realistic plan. Do people want an ocean view and cool breeze in some places? How does the forest affect privacy? Where is the shoreline eroding? What areas most need protection? Does the whole community understand the benefits of protecting the shoreline?</li>
<li>Respectfully consult landowners and leaders.</li>
<li>Also involve children, groundskeepers, and anyone who might affect the project day-to-day.</li>
<li>Some people are constantly “cleaning” their land by uprooting grass, vines and young plants, but that leaves the soil unprotected; trees alone are not enough. Encourage people to see the beauty or practicality of dense vegetation.</li>
<li>Support laws that protect coastal vegetation (restrict its removal).</li>
<li>Support laws that restrict mining of sand and rock aggregate.</li>
</ul>

<h2>Work with nature</h2>

<ul>
<li>Conserve existing forest. Some trees and shrubs are very old and would take many years to replace. If plants are removed, and the soil is washed away, it may not be possible to replant in the same place.</li>
<li>Help nature to replant natural forest. In some places, roots and seeds are naturally regrowing. Protect natural regrowth or desirable plants as carefully as you would protect newly planted seedlings. Mark them and weed them.</li>
<li>Encourage shrubs and vines as well as trees, so that the dense vegetation will hold the soil, block wind and break waves.</li>
<li>Large trees can cause more harm than good if planted too close to the ocean, because they disturb sand if they fall, and currents may scour sand from around their roots and trunks.</li>
<li>Care for each species where it naturally belongs in the <a href="strand-vegetation.php">strand forest</a> and <a href="http:mangroves.php">mangrove</a>.
<li>Remember that plants can slow down but not completely stop natural erosion. For a more complete explanation of coastal change, get  the "Coastal Change in the Pacific Islands" book distributed by CMI, or download it (<a href="http://www.reefresilience.org/wp-content/uploads/Gombos-et-al.-2014-Coastal-Change-in-the-Pacific-Islands.pdf">8MB</a>).</li>
</ul>

