<p><em>Experience from <a href="acknowledgements.php">Sleeper Sared</a></em></p>

<p>On three atoll islets, Sleeper planted mangroves along the lagoon-side shore in a strip one mile long. He planted four rows, with seedlings 2–3 feet apart. He gathered the propagules (sprouted seeds) locally and planted them directly into the sand or sediment. After one year, 75% (three quarters) were surviving. Sleeper says seagrass is an indicator of sufficiently calm waters with enough sediment.</p>
<p>On another atoll islet, the mangroves all died, because the currents were too strong.</p>
<p>Sleeper says: If there is a current and not enough sediment to hold the propagules when they are planted, put out rocks or wire boxes filled with rocks. Wait for sediments to be deposited by the currents. Plant propagules into the accumulated sediments.</p>
<p>Sleeper says: Another technique for areas without much sediment is to use heavy paper bags such as cement sacks or some rice sacks. (Ordinary paper bags are not strong enough.) Fill the bags with sand and seaweed and plant propagules in them in a nursery. They are ready after they take root in the sacks, but before roots burst through sacks and root in the ground. Outplant them in holes dug into sediment or in niches between rocks.</p>

<h4>Planning to plant eoeak on the coast of Ailuk</h4>

<p>The Ailuk Atoll Marine Resource Management Advisory Committee was interested in planting mangroves on the coast of Ailuk to reduce shoreline erosion. Rhizophora was selected as the most appropriate species for coastal planting, but it would have to be brought from other atolls. A planting site was chosen (1) between high and low tide (2) at a spot sheltered from wind and waves (3) with soft sediments good for planting propagules (4) from where the tide flowed into the lagoon, not the ocean. The project’s <a href="http://www.nras-conservation.org/nraslibrary/EllisonReport02.07.pdf" target="_blank">Natural Resource Assessment Survey</a> explains the considerations and plans for mangrove planting.</p>

<h4>Planting mangroves in Kiribati</h4>

<p>Kiribati, like the Marshalls, is a nation of atolls, where sea level rise and coastal erosion are of great concern. Mangroves are an important resource in Kiribati and a mangrove planting program has been ongoing for at least twenty years. <em>Rhizophora stylosa</em> (a close relative of eoeak) is one of the most important species for planting. Propagules are planted along shorelines and causeways. For a short description of Kiribati mangrove planting, see this <a href="http://www.glomis.com/news/newsletter/ISMENews39(201403).pdf" target="_blank">newsletter</a> from the International Society for Mangrove Ecosystems.</p>

