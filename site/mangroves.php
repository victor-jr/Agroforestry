<h3>Where to plant: different species belong in different zones</h3>
<ul>
<li><a href="http://www.hawaii.edu/cpis/MI/plants/bulabol.html">Eoeak or Būļaboļ (Rhizophora)</a> is called a <em>fringe</em> species because it can grow on the seaward edge of a mangrove forest. Plant large propagules at mean sea level, about halfway between the high tide and low tide points, where it will be inundated by any day’s high tide. If it is an <a href=".">El Niño year</a>, the sea levels might be unusually low, so plant them where it is shallow, because when normal weather returns, the water will be deeper.</li>
<li><a href="http://www.hawaii.edu/cpis/MI/plants/bulabol_(konpat).html">Kōnpat or Būļabōļ (Sonneratia)</a> and <a href="http://www.hawaii.edu/cpis/MI/plants/jon.html">Joñ (Bruguiera)</a> are usually found in inland swamps (<em>pat</em>) in the Marshalls. You could try planting large propagules of joñ closer to shore than the eoeak, where they will be flooded by medium-high tides.</li>
<li><a href="http://www.hawaii.edu/cpis/MI/plants/kimeme.html">Kimeme (Lumnitzera)</a> is called an <em>interior</em> species. It is also mostly found in <em>pat</em>. You can try planting kimeme seedlings close to the shoreline, where only high tides will flood them.</li>
<li><a href="http://www.hawaii.edu/cpis/MI/plants/kone.html">Kōñe (Pemphis)</a> is a <em>mangrove associate</em> that grows closest to shore but can tolerate flooding by the highest tides.</li>
</ul>

